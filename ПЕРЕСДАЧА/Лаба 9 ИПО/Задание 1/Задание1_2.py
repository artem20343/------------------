def to_octal(num):
    """Переводит число в 10-значный восьмеричный код с сохранением лидирующих нулей."""
    o = oct(num)[2:]  # Получаем восьмеричное представление и убираем префикс '0o'
    p = o.zfill(10)  # Добавляем лидирующие нули до 10 символов
    return p

# Пример использования
num = int(input("Введите неотрицательное целое число: "))

if num < 0:
    print("Число должно быть неотрицательным.")
else:
    n = to_octal(num)
    print(f"10-значное восьмеричное представление числа {num}: {n}")
